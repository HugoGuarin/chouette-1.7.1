package fr.certu.chouette.service.importateur.multilignes.pegase;

public class HoraireException extends Exception {
	
	public HoraireException() {
		super();
	}
	
	public HoraireException(String message) {
		super(message);
	}
	
	public HoraireException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public HoraireException(Throwable cause) {
		super(cause);
	}
}
